#!/usr/bin/env node
/**
 * Move feed-detail.yml entries whose id no longer appears in
 * feed-scored.json into feed-detail-archive.yml, keeping the main
 * file from growing without bound as items roll off the feed.
 *
 * Run manually (e.g. once a quarter) — not in cron, because cron-time
 * pruning would interact badly with a transient extract failure that
 * left feed-scored.json half-empty.
 *
 * Flags:
 *   --dry-run   show what would move, don't write files
 *   --keep-recent  also keep entries whose feed-scored row has
 *                  publishedAt within the last N days (default 90)
 */

import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import yaml from "js-yaml";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, "..");

const DETAIL = path.join(projectRoot, "content/feed-detail.yml");
const ARCHIVE = path.join(projectRoot, "content/feed-detail-archive.yml");
const SCORED = path.join(projectRoot, "src/data/generated/feed-scored.json");

const argv = new Set(process.argv.slice(2));
const DRY = argv.has("--dry-run");
const KEEP_RECENT_DAYS = (() => {
  for (const a of process.argv.slice(2)) {
    if (a.startsWith("--keep-recent=")) return parseInt(a.split("=")[1], 10);
  }
  return 90;
})();

async function readYamlOrEmpty(file) {
  try {
    const text = await fs.readFile(file, "utf8");
    return yaml.load(text) || {};
  } catch {
    return {};
  }
}

function dumpYaml(obj, headerComment) {
  // Preserve readable multi-line strings using yaml's literal style for
  // long values.
  const body = yaml.dump(obj, {
    lineWidth: 1000,
    quotingType: '"',
    styles: { "!!str": "literal" },
  });
  return headerComment ? headerComment + "\n" + body : body;
}

async function main() {
  const detail = await readYamlOrEmpty(DETAIL);
  const archive = await readYamlOrEmpty(ARCHIVE);
  let scored;
  try {
    scored = JSON.parse(await fs.readFile(SCORED, "utf8"));
  } catch {
    console.error("feed-scored.json missing or unreadable — abort to be safe");
    process.exit(1);
  }

  const liveIds = new Set(scored.items.map((it) => it.id));
  const recentThreshold = Date.now() - KEEP_RECENT_DAYS * 86400 * 1000;
  const byIdInScored = new Map(scored.items.map((it) => [it.id, it]));

  const detailIds = Object.keys(detail).filter(
    (k) => !k.startsWith("# ") && typeof detail[k] === "string",
  );
  const toArchive = [];
  const kept = {};

  for (const id of detailIds) {
    if (!liveIds.has(id)) {
      // Not in current feed-scored → candidate for archive unless we still
      // remember it being published recently (defensive: scored could have
      // been pruned for a different reason).
      const scoredRow = byIdInScored.get(id);
      const ts = scoredRow?.publishedAt
        ? Date.parse(scoredRow.publishedAt)
        : null;
      if (ts && ts > recentThreshold) {
        kept[id] = detail[id];
      } else {
        toArchive.push(id);
      }
    } else {
      kept[id] = detail[id];
    }
  }

  // Merge into archive (preserve prior archive entries verbatim)
  const nextArchive = { ...archive };
  for (const id of toArchive) {
    nextArchive[id] = detail[id];
  }

  console.log(
    `prune-feed-detail: detail=${detailIds.length}, live=${liveIds.size}, would-archive=${toArchive.length}`,
  );
  if (toArchive.length > 0) {
    console.log("ids to archive:");
    for (const id of toArchive) console.log("  " + id);
  }
  if (DRY) {
    console.log("(dry-run — no files written)");
    return;
  }

  if (toArchive.length === 0) {
    console.log("nothing to do");
    return;
  }

  await fs.writeFile(
    DETAIL,
    dumpYaml(
      kept,
      "# Hand-written deep-dive paragraphs for /latest item detail pages.\n# See header comments at the top of this file for format details.",
    ),
  );
  await fs.writeFile(
    ARCHIVE,
    dumpYaml(
      nextArchive,
      "# Archived deep-dive entries whose source items rolled off feed-scored.json.\n# Safe to inspect or restore manually. Generated by scripts/prune-feed-detail.mjs.",
    ),
  );
  console.log(`wrote ${kept ? Object.keys(kept).length : 0} kept, ${Object.keys(nextArchive).length} archived`);
}

main().catch((err) => {
  console.error("prune-feed-detail failed:", err);
  process.exit(1);
});
